// Auth checks
// ─────────────────────────────────────────────────────────────────────────────

/// Check if GITHUB_PERSONAL_ACCESS_TOKEN is available in env or ~/.secrets
fn check_github_pat() -> (bool, String) {
    // First check env
    if let Ok(v) = std::env::var("GITHUB_PERSONAL_ACCESS_TOKEN") {
        if !v.is_empty() {
            return (
                true,
                format!("env:$GITHUB_PERSONAL_ACCESS_TOKEN ({} chars)", v.len()),
            );
        }
    }

    // Try ~/.secrets
    if let Some(home) = dirs::home_dir() {
        let secrets = home.join(".secrets");
        if let Ok(content) = fs::read_to_string(&secrets) {
            for line in content.lines() {
                let line = line.trim();
                if line.starts_with("GITHUB_PERSONAL_ACCESS_TOKEN=") {
                    let value = line.split_once('=').map(|(_, v)| v.trim()).unwrap_or("");
                    if !value.is_empty() && !value.to_lowercase().contains("router") {
                        return (true, format!("~/.secrets ({} chars)", value.len()));
                    }
                }
            }
        }
    }

    (
        false,
        "GITHUB_PERSONAL_ACCESS_TOKEN not found in env or ~/.secrets".into(),
    )
}

/// Check if Cloudflare OAuth has been completed (cache file exists)
fn check_cloudflare_oauth() -> (bool, String) {
    let cache_path = match cloudflare_auth_cache() {
        Ok(p) => p,
        Err(_) => return (false, "cannot determine cache path".into()),
    };

    if cache_path.exists() {
        // Check if it's non-empty
        if let Ok(content) = fs::read_to_string(&cache_path) {
            if !content.trim().is_empty() {
                return (true, format!("cached at {}", cache_path.display()));
            }
        }
    }

    (false, format!("no OAuth cache at {}", cache_path.display()))
}

// ─────────────────────────────────────────────────────────────────────────────
// Plugin discovery
// ─────────────────────────────────────────────────────────────────────────────

/// Read enabled plugins from Claude Code settings.json
fn enabled_plugins_from_settings() -> Result<Vec<String>> {
    let path = settings_path()?;
    if !path.exists() {
        return Ok(Vec::new());
    }
    let content = fs::read_to_string(&path)?;
    let settings: serde_json::Value = serde_json::from_str(&content)?;

    // Try the new MCP format: mcpServers
    if let Some(servers) = settings.get("mcpServers").and_then(|v| v.as_object()) {
        let names: Vec<String> = servers.keys().cloned().collect();
        if !names.is_empty() {
            return Ok(names);
        }
    }

    // Fall back to legacy plugins array
    if let Some(plugins) = settings.get("plugins").and_then(|v| v.as_array()) {
        let names: Vec<String> = plugins
            .iter()
            .filter_map(|v| v.as_str().map(String::from))
            .collect();
        if !names.is_empty() {
            return Ok(names);
        }
    }

    // Try enabledPlugins (another legacy format)
    if let Some(plugins) = settings.get("enabledPlugins").and_then(|v| v.as_array()) {
        let names: Vec<String> = plugins
            .iter()
            .filter_map(|v| v.as_str().map(String::from))
            .collect();
        if !names.is_empty() {
            return Ok(names);
        }
    }

    Ok(Vec::new())
}

/// Get the list of known plugin MCP configs from the plugin cache directory
fn cached_plugin_configs() -> Result<HashMap<String, PathBuf>> {
    let cache = plugin_cache_dir()?;
    let mut configs = HashMap::new();

    if !cache.exists() {
        return Ok(configs);
    }

    for entry in fs::read_dir(&cache)? {
        let entry = entry?;
        let name = entry.file_name();
        let name_str = name.to_string_lossy();

        // Look for .mcp.json in the plugin directory
        // Pattern: <cache>/<org>/<plugin>/<version>/.mcp.json
        if entry.file_type()?.is_dir() {
            // Try direct .mcp.json in the plugin dir
            let mcp_path = entry.path().join(".mcp.json");
            if mcp_path.exists() {
                configs.insert(name_str.to_string(), mcp_path);
                continue;
            }

            // Try nested: <org>/<plugin>/<version>/.mcp.json
            for sub in fs::read_dir(entry.path())? {
                let sub = sub?;
                if sub.file_type()?.is_dir() {
                    let mcp_path = sub.path().join(".mcp.json");
                    if mcp_path.exists() {
                        configs.insert(name_str.to_string(), mcp_path);
                    }
                }
            }
        }
    }

    Ok(configs)
}

// ─────────────────────────────────────────────────────────────────────────────
// Main check function
// ─────────────────────────────────────────────────────────────────────────────

/// Run all plugin auth checks and return a summary
pub fn check_all() -> Result<PluginSummary> {
    let plugins = enabled_plugins_from_settings()?;
    let cached = cached_plugin_configs()?;

    // Deduplicate + merge
    let mut all_names: Vec<String> = plugins;
    for name in cached.keys() {
        if !all_names.contains(name) {
            all_names.push(name.clone());
        }
    }
    all_names.sort();
    all_names.dedup();

    let mut checks = Vec::new();
    let mut github_ok = false;
    let mut cloudflare_ok = false;

    for name in &all_names {
        let auth = PluginAuth::for_plugin(name);
        let (status, detail) = match &auth {
            PluginAuth::GitHubPat => {
                let (ok, detail) = check_github_pat();
                if ok {
                    github_ok = true;
                }
                (PluginStatus::Ok, detail)
            }
            PluginAuth::CloudflareOAuth => {
                let (ok, detail) = check_cloudflare_oauth();
                if ok {
                    cloudflare_ok = true;
                }
                (PluginStatus::Ok, detail)
            }
            PluginAuth::None => (PluginStatus::Ok, "no auth required".into()),
            PluginAuth::Unknown => {
                if cached.contains_key(name) {
                    (
                        PluginStatus::Ok,
                        "plugin configured (unknown auth type)".into(),
                    )
                } else {
                    (PluginStatus::NotFound, "plugin not found in cache".into())
                }
            }
        };

        checks.push(PluginCheck {
            name: name.clone(),
            auth,
            status,
            detail,
        });
    }

    let all_ok = checks.iter().all(|c| c.status == PluginStatus::Ok);

    Ok(PluginSummary {
        checks,
        github_ok,
        cloudflare_ok,
        all_ok,
    })
}

/// Pretty-print the plugin summary to stdout
pub fn print_summary(summary: &PluginSummary) {
    println!();
    println!("  Plugin Auth Status");
    println!("  ─────────────────");

    for check in &summary.checks {
        let icon = match check.status {
            PluginStatus::Ok => "OK",
            PluginStatus::Missing => "MISSING",
            PluginStatus::NotFound => "NOT FOUND",
        };

        let auth_tag = match &check.auth {
            PluginAuth::GitHubPat => "[GitHub PAT]",
            PluginAuth::CloudflareOAuth => "[Cloudflare OAuth]",
            PluginAuth::None => "",
            PluginAuth::Unknown => "",
        };

        println!("  {:<30} {:<12} {}", check.name, icon, auth_tag);
        if !check.detail.is_empty() && check.detail != "no auth required" {
            println!("    └─ {}", check.detail);
        }
    }

    println!();
    println!(
        "  GitHub MCP:    {}",
        if summary.github_ok { "OK" } else { "MISSING" }
    );
    println!(
        "  Cloudflare MCP: {}",
        if summary.cloudflare_ok {
            "OK"
        } else {
            "MISSING"
        }
    );
    println!();
}

// ─────────────────────────────────────────────────────────────────────────────
// CLI command
// ─────────────────────────────────────────────────────────────────────────────

/// Entry point for `ccb plugins` command
pub fn run() -> Result<()> {
    let summary = check_all()?;
    print_summary(&summary);

    if !summary.all_ok {
        std::process::exit(1);
    }
    Ok(())
}

