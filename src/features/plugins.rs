//! Plugin auth wiring — CCB Story 023
//!
//! Inspects Claude Code plugin configurations, checks auth state for GitHub MCP
//! and Cloudflare MCP plugins, and reports connection status.
//!
//! ## What it checks
//!
//! **GitHub MCP** — looks for `GITHUB_PERSONAL_ACCESS_TOKEN` in env / ~/.secrets
//! and verifies the MCP config exists at the plugin cache path.
//!
//! **Cloudflare MCP** — verifies OAuth has been completed by checking for
//! a cached token file at `~/.claude/plugins/.cloudflare-auth`.
//!
//! ## Output
//!
//! Lists all enabled plugins, marks GitHub and Cloudflare auth as OK / MISSING,
//! and summarizes connection status. Exit code 0 means all checks pass.

use anyhow::{Context, Result};
use std::collections::HashMap;
use std::fs;
use std::path::PathBuf;

// ─────────────────────────────────────────────────────────────────────────────
// Plugin types
// ─────────────────────────────────────────────────────────────────────────────

/// Known plugin types and their auth requirements
#[derive(Debug, Clone, PartialEq)]
pub enum PluginAuth {
    /// No authentication required
    None,
    /// GitHub PAT via GITHUB_PERSONAL_ACCESS_TOKEN
    GitHubPat,
    /// Cloudflare OAuth (browser-based, cached after first flow)
    CloudflareOAuth,
    /// Unknown plugin type
    Unknown,
}

impl PluginAuth {
    fn for_plugin(name: &str) -> Self {
        match name {
            "github" => PluginAuth::GitHubPat,
            "cloudflare-api"
            | "cloudflare-bindings"
            | "cloudflare-builds"
            | "cloudflare-observability"
            | "cloudflare-docs" => PluginAuth::CloudflareOAuth,
            "playwright" | "hookify" => PluginAuth::None,
            _ => PluginAuth::Unknown,
        }
    }
}

/// Result of checking a single plugin's auth state
#[derive(Debug)]
pub struct PluginCheck {
    pub name: String,
    pub auth: PluginAuth,
    pub status: PluginStatus,
    pub detail: String,
}

/// Plugin auth status
#[derive(Debug, Clone, PartialEq)]
pub enum PluginStatus {
    /// Authenticated / configured
    Ok,
    /// Auth required but not yet set up
    Missing,
    /// Plugin binary/config not found
    NotFound,
}

impl std::fmt::Display for PluginStatus {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            PluginStatus::Ok => write!(f, "OK"),
            PluginStatus::Missing => write!(f, "MISSING"),
            PluginStatus::NotFound => write!(f, "NOT FOUND"),
        }
    }
}

/// Summary of all plugin checks
#[derive(Debug)]
pub struct PluginSummary {
    pub checks: Vec<PluginCheck>,
    pub github_ok: bool,
    pub cloudflare_ok: bool,
    pub all_ok: bool,
}

// ─────────────────────────────────────────────────────────────────────────────
// Paths
// ─────────────────────────────────────────────────────────────────────────────

fn claude_home() -> Result<PathBuf> {
    dirs::home_dir()
        .context("cannot determine home directory")
        .map(|h| h.join(".claude"))
}

fn settings_path() -> Result<PathBuf> {
    Ok(claude_home()?.join("settings.json"))
}

fn plugin_cache_dir() -> Result<PathBuf> {
    Ok(claude_home()?
        .join("plugins")
        .join("cache")
        .join("claude-plugins-official"))
}

/// Path where Cloudflare OAuth tokens are cached
fn cloudflare_auth_cache() -> Result<PathBuf> {
    Ok(claude_home()?.join("plugins").join(".cloudflare-auth"))
}

/// Path to the GitHub MCP config JSON (as generated by the plugin)
fn github_mcp_config() -> Result<PathBuf> {
    let home = dirs::home_dir().context("no home dir")?;
    // The GitHub plugin creates this path when first initialized
    let candidates = [
        home.join(".claude")
            .join("plugins")
            .join("cache")
            .join("claude-plugins-official")
            .join("github")
            .join("unknown")
            .join(".mcp.json"),
        home.join(".claude")
            .join("plugins")
            .join("cache")
            .join("github")
            .join(".mcp.json"),
    ];
    candidates
        .iter()
        .find(|p| p.exists())
        .map(|p| p.clone())
        .with_context(|| format!("GitHub MCP config not found in any of: {:?}", candidates))
}

