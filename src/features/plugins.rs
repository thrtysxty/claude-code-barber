#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn plugin_auth_classification() {
        assert_eq!(PluginAuth::for_plugin("github"), PluginAuth::GitHubPat);
        assert_eq!(
            PluginAuth::for_plugin("cloudflare-api"),
            PluginAuth::CloudflareOAuth
        );
        assert_eq!(
            PluginAuth::for_plugin("cloudflare-bindings"),
            PluginAuth::CloudflareOAuth
        );
        assert_eq!(
            PluginAuth::for_plugin("cloudflare-builds"),
            PluginAuth::CloudflareOAuth
        );
        assert_eq!(
            PluginAuth::for_plugin("cloudflare-observability"),
            PluginAuth::CloudflareOAuth
        );
        assert_eq!(
            PluginAuth::for_plugin("cloudflare-docs"),
            PluginAuth::CloudflareOAuth
        );
        assert_eq!(PluginAuth::for_plugin("playwright"), PluginAuth::None);
        assert_eq!(PluginAuth::for_plugin("hookify"), PluginAuth::None);
        assert_eq!(
            PluginAuth::for_plugin("unknown-plugin"),
            PluginAuth::Unknown
        );
    }

    #[test]
    fn github_pat_check_returns_tuple() {
        let (ok, detail) = check_github_pat();
        assert!(!detail.is_empty());
        // ok may be true or false depending on env setup, both are valid
        let _ = (ok, detail);
    }

    #[test]
    fn cloudflare_oauth_check_returns_tuple() {
        let (ok, detail) = check_cloudflare_oauth();
        assert!(!detail.is_empty());
        // ok may be true or false depending on auth state, both are valid
        let _ = (ok, detail);
    }

    #[test]
    fn enabled_plugins_from_nonexistent_settings() {
        // When settings.json doesn't exist, should return empty vec (not error)
        let tmp = tempfile::tempdir().unwrap();
        // This test relies on settings.json not existing at the real path,
        // so we just verify the function doesn't panic
        let result = std::panic::catch_unwind(|| enabled_plugins_from_settings());
        assert!(result.is_ok());
    }

    #[test]
    fn plugin_status_display() {
        assert_eq!(PluginStatus::Ok.to_string(), "OK");
        assert_eq!(PluginStatus::Missing.to_string(), "MISSING");
        assert_eq!(PluginStatus::NotFound.to_string(), "NOT FOUND");
    }

    #[test]
    fn check_all_produces_summary() {
        // Verify check_all doesn't panic and produces expected fields
        let summary = check_all().expect("check_all should not fail");
        assert!(summary.checks.iter().all(|c| !c.name.is_empty()));
        assert!(!summary.all_ok || summary.checks.iter().all(|c| c.status == PluginStatus::Ok));
    }

    #[test]
    fn cached_plugin_configs_returns_hashmap() {
        // Should not panic even if cache dir doesn't exist
        let result = cached_plugin_configs();
        assert!(result.is_ok());
        let configs = result.unwrap();
        // Returns empty map if dir doesn't exist
        assert!(configs.is_empty() || !configs.is_empty()); // always true, just checking no panic
    }
}
