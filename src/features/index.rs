use std::fmt::Write as FmtWrite;
use std::path::{Path, PathBuf};

pub struct IndexEntry {
    pub name: String,
    pub category: String,
    pub triggers: String,
    pub path: String,
}

pub fn build(skills_dir: &Path) -> anyhow::Result<String> {
    let mut entries: Vec<IndexEntry> = Vec::new();

    for entry in std::fs::read_dir(skills_dir)? {
        let entry = entry?;
        let skill_md = entry.path().join("SKILL.md");
        if !skill_md.exists() {
            continue;
        }

        let name = entry.file_name().to_string_lossy().to_string();
        let content = std::fs::read_to_string(&skill_md).unwrap_or_default();
        let category = infer_category(&name);
        let triggers = extract_triggers(&content);
        let rel_path = format!("skills/{}/SKILL.md", name);

        entries.push(IndexEntry {
            name,
            category: category.to_string(),
            triggers,
            path: rel_path,
        });
    }

    entries.sort_by(|a, b| a.category.cmp(&b.category).then(a.name.cmp(&b.name)));
    Ok(render(&entries))
}

fn render(entries: &[IndexEntry]) -> String {
    let mut out = String::new();
    writeln!(out, "# Skills Index").unwrap();
    writeln!(out, "<!-- generated by ccb — do not edit manually -->").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "| name | category | triggers | path |").unwrap();
    writeln!(out, "|------|----------|----------|------|").unwrap();
    for e in entries {
        writeln!(
            out,
            "| {} | {} | {} | {} |",
            e.name, e.category, e.triggers, e.path
        )
        .unwrap();
    }
    out
}

fn infer_category(name: &str) -> &'static str {
    let rules: &[(&[&str], &str)] = &[
        (
            &["commit", "push", "pr", "git", "branch", "merge", "clean"],
            "git",
        ),
        (
            &["read", "write", "edit", "file", "path", "cat"],
            "file-ops",
        ),
        (
            &[
                "codebase",
                "graph",
                "trace",
                "search",
                "memory",
                "explore",
                "quality",
                "reference",
                "tracing",
            ],
            "codebase",
        ),
        (
            &["deploy", "docker", "pi", "server", "infra", "pi_check"],
            "deploy",
        ),
        (
            &[
                "agent",
                "persona",
                "junior",
                "senior",
                "architect",
                "create",
                "product",
                "owner",
            ],
            "agents",
        ),
        (
            &[
                "skill", "hook", "config", "meta", "index", "update", "scaffold", "hookify",
            ],
            "meta",
        ),
        (
            &["test", "tdd", "verify", "security", "review", "careful"],
            "quality",
        ),
        (
            &["story", "dispatch", "handoff", "log", "ledger"],
            "project",
        ),
    ];
    for (keywords, cat) in rules {
        if keywords.iter().any(|k| name.contains(k)) {
            return cat;
        }
    }
    "misc"
}

fn extract_triggers(content: &str) -> String {
    for line in content.lines() {
        if line.trim_start().starts_with("description:") {
            let desc = line.trim_start_matches("description:").trim();
            let summary: String = desc.chars().take(60).collect();
            return summary.trim_end_matches('.').to_string();
        }
    }
    String::new()
}

pub fn write(skills_dir: &Path) -> anyhow::Result<PathBuf> {
    let content = build(skills_dir)?;
    let index_path = skills_dir.join("INDEX.md");
    std::fs::write(&index_path, &content)?;
    Ok(index_path)
}
